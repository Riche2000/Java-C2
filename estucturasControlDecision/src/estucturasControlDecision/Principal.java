package estucturasControlDecision;

public class Principal {

	public static void main(String[] args) {
		//Estructuras control
		int edad=19;
		//Si edad es mayor a 18
		if(edad>18) {
			System.out.println("Tiene más de 18 años");
		}
		else if(edad>16) {
			System.out.println("Tiene más de 16 años");
		}
		else {
			System.out.println("Tiene menos de 16 años");
		}

	}
}
